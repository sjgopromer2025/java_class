package 컬렉션프레임워크;

public class CollectionFrameworkIntro {
    public static void main(String[] args) {
        System.out.println("컬렉션 프레임워크는 데이터를 효율적으로 저장하고 관리하기 위한 표준화된 클래스의 집합입니다.");
    }
}

/*
 * 이론 설명:
 * 1. 컬렉션 프레임워크란?
 * - 자바에서 데이터를 저장하고 조작하기 위한 표준화된 클래스와 인터페이스의 집합.
 * - 데이터를 효율적으로 저장, 검색, 삭제, 정렬할 수 있는 다양한 자료구조를 제공합니다.
 * 
 * 2. 주요 특징:
 * - 데이터 구조와 알고리즘을 제공하여 개발자가 직접 구현할 필요를 줄임.
 * - 다양한 데이터 구조를 일관된 방식으로 사용할 수 있도록 인터페이스 기반으로 설계됨.
 * 
 * 3. 주요 인터페이스:
 * - `Collection`: 컬렉션의 기본 인터페이스.
 * - `List`: 순서가 있는 데이터의 집합.
 * - `Set`: 중복을 허용하지 않는 데이터의 집합.
 * - `Queue`: FIFO(First In First Out) 방식의 데이터 구조.
 * - `Map`: 키-값 쌍으로 데이터를 저장하는 구조.
 */