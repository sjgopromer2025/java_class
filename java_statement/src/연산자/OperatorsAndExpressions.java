package 연산자;

public class OperatorsAndExpressions {
    public static void main(String[] args) {
        // 연산자와 연산식 예제
        int a = 10;
        int b = 20;
        int sum = a + b; // 덧셈 연산자
        int product = a * b; // 곱셈 연산자

        System.out.println("덧셈 결과: " + sum); // 30
        System.out.println("곱셈 결과: " + product); // 200

        boolean isEqual = (a == b); // 비교 연산자
        System.out.println("a와 b가 같은가? " + isEqual); // false
    }
}

